set IsActive = iif(v.VersionNumber = 2,1,0),
DefaultVersion = iif(v.AlternativeNo = 1 and v.VersionNumber = 2,1,0)
from dbo.RouteVersions as v
inner join dbo.Products as pr
on pr.Id = v.ProductId
